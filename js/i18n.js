// ── Language detection ────────────────────────────────────────────────────────
function detectBrowserLang() {
  const nav = (navigator.languages && navigator.languages[0]) || navigator.language || 'en';
  const l = nav.toLowerCase();
  if (l.startsWith('zh-tw') || l.startsWith('zh-hk') || l.startsWith('zh-mo')) return 'zh-TW';
  if (l.startsWith('zh')) return 'zh-CN';
  if (l.startsWith('ja')) return 'ja';
  if (l.startsWith('ko')) return 'ko';
  if (l.startsWith('fr')) return 'fr';
  if (l.startsWith('de')) return 'de';
  if (l.startsWith('es')) return 'es';
  if (l.startsWith('pt')) return 'pt-BR';
  return 'en';
}

function getLang() {
  return localStorage.getItem('lang') || detectBrowserLang();
}

function applyLang(lang) {
  if (!T[lang]) lang = 'en';
  const t = T[lang];
  // text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });
  // html content (for links in policy sections)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  // update screenshots
  const folder = SS_FOLDER[lang] || 'en-US';
  document.querySelectorAll('[data-screenshot]').forEach(img => {
    img.src = '/img/screenshots/' + folder + '/' + img.dataset.screenshot;
  });
  // update html lang & button
  document.documentElement.lang = lang;
  const btn = document.getElementById('btnLang');
  if (btn) btn.textContent = LANGS[lang] || lang;
  localStorage.setItem('lang', lang);
}
